def _save_ablation_summary(
    results: dict[str, AblationRunResult],
    gold: dict[str, list[int]],
    output_path: Path,
) -> None:
    """Save ablation summary JSON and comparison report."""
    summary = {}
    for config_name, run_result in results.items():
        m = run_result.final_metrics(gold)
        summary[config_name] = {
            "description": run_result.description,
            "config": {
                "prompt_anchors": ABLATION_CONFIGS[config_name].prompt_anchors,
                "level_b": ABLATION_CONFIGS[config_name].level_b,
                "level_c": ABLATION_CONFIGS[config_name].level_c,
            },
            "final_metrics": {
                inst: {k: round(v, 4) if isinstance(v, float) else v
                       for k, v in inst_metrics.items()
                       if isinstance(v, (int, float, bool, str))}
                for inst, inst_metrics in m.items()
                if isinstance(inst_metrics, dict)
            },
            "trajectory": run_result.per_round_metrics(gold),
        }

    summary_path = output_path / "ablation_summary.json"
    summary_path.write_text(json.dumps(summary, indent=2, ensure_ascii=False))
    logger.info("Ablation summary saved to %s", summary_path)

    report = format_ablation_comparison(results, gold)
    report_path = output_path / "ablation_report.txt"
    report_path.write_text(report, encoding="utf-8")
    logger.info("Ablation report saved to %s", report_path)
    print(report)
