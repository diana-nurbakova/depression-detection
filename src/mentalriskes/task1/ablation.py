# Post-hoc calibration ablation (applies B/C to existing prediction logs)
# ---------------------------------------------------------------------------

def posthoc_calibration_ablation(
    raw_log_path: str | Path,
    gold: dict[str, list[int]] | None = None,
    apply_level_b: bool = True,
    apply_level_c: bool = False,
    client: LLMClient | None = None,
) -> dict:
    """Apply Level B and/or C post-hoc to existing prediction logs.

    Reads raw scores from a JSONL prediction log and applies the
    calibration tiers without re-running the LLM assessors.

    Limitation: cannot retroactively apply Level A (prompt anchors change
    the LLM input and thus the raw scores themselves).

    Args:
        raw_log_path: path to predictions_*.jsonl from a prior trial run.
        gold: gold scores (defaults to TRIAL_GOLD).
        apply_level_b: apply Level B constraints.
        apply_level_c: apply Level C LLM agent (requires client).
        client: LLMClient for Level C (required if apply_level_c=True).

    Returns:
        dict with per-round results and final metrics.
    """
    gold = gold or TRIAL_GOLD
    raw_path = Path(raw_log_path)

    rows = []
    with open(raw_path, "r", encoding="utf-8") as f:
        for line in f:
            rows.append(json.loads(line))

    processed = []
    for entry in rows:
        phq9 = entry["phq9"]
        gad7 = entry["gad7"]
        compact10 = entry["compact10"]
        round_n = entry.get("round", 0)
        session_id = entry.get("session_id", "")

        violations: list[ConstraintViolation] = []
        if apply_level_b:
            phq9, gad7, compact10, violations = apply_level_b_constraints(phq9, gad7, compact10)

        level_c_corrections: list[dict] = []
        if apply_level_c and client and _should_invoke_level_c(
            violations, compact10, sum(phq9), sum(gad7),
        ):
            phq9, gad7, compact10, level_c_corrections = run_level_c_agent(
                client, phq9, gad7, compact10, violations,
            )

        processed.append({
            "session_id": session_id,
            "round": round_n,
            "phq9": phq9,
            "gad7": gad7,
            "compact10": compact10,
            "level_b_violations": [v.rule for v in violations],
            "level_c_corrections": len(level_c_corrections),
        })

    # Find last prediction per session
    last_preds: dict[str, dict] = {}
    for entry in processed:
        last_preds[entry["session_id"]] = entry

    # Evaluate
    results_per_session = {}
    for sid, entry in last_preds.items():
        predicted = {
            "PHQ-9": entry["phq9"],
            "GAD-7": entry["gad7"],
            "CompACT-10": entry["compact10"],
        }
        results_per_session[sid] = evaluate_task1_full(predicted, gold)

    return {
        "source_log": str(raw_path),
        "apply_level_b": apply_level_b,
        "apply_level_c": apply_level_c,
        "n_rounds": len(rows),
        "per_round": processed,
        "final_metrics": results_per_session,
    }


