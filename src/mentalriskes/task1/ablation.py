@dataclass
class RoundResult:
    """Assessment result for one session at one round."""
    session_id: str
    round_number: int
    phq9: list[int]
    gad7: list[int]
    compact10: list[int]
    level_b_violations: list[ConstraintViolation] = field(default_factory=list)
    level_c_corrections: list[dict] = field(default_factory=list)
    elapsed_s: float = 0.0


@dataclass
class AblationRunResult:
    """Full result for a single ablation configuration on trial data."""
    config_name: str
    description: str
    round_results: list[RoundResult] = field(default_factory=list)

    @property
    def final_result(self) -> RoundResult | None:
        """Last round result (for final evaluation against gold)."""
        return self.round_results[-1] if self.round_results else None

    def per_round_metrics(self, gold: dict[str, list[int]] | None = None) -> list[dict]:
        """RMSE per instrument per round (convergence trajectory)."""
        gold = gold or TRIAL_GOLD
        rows = []
        for rr in self.round_results:
            predicted = {"PHQ-9": rr.phq9, "GAD-7": rr.gad7, "CompACT-10": rr.compact10}
            metrics = evaluate_task1_full(predicted, gold)
            rows.append({
                "round": rr.round_number,
                "rmse_phq9": metrics["PHQ-9"]["rmse"],
                "rmse_gad7": metrics["GAD-7"]["rmse"],
                "rmse_compact10": metrics["CompACT-10"]["rmse"],
                "mean_rmse": metrics["_overall"]["mean_rmse"],
                "mean_pearson": metrics["_overall"]["mean_pearson"],
                "va_mean": sum(rr.compact10[i] for i in [1, 3, 6, 9]) / 4,
                "level_b_violations": len(rr.level_b_violations),
                "level_c_corrections": len(rr.level_c_corrections),
            })
        return rows

    def final_metrics(self, gold: dict[str, list[int]] | None = None) -> dict:
        """Full evaluation of the final-round prediction."""
        gold = gold or TRIAL_GOLD
        if not self.final_result:
            return {}
        predicted = {
            "PHQ-9": self.final_result.phq9,
            "GAD-7": self.final_result.gad7,
            "CompACT-10": self.final_result.compact10,
        }
        return evaluate_task1_full(predicted, gold)


