def format_ablation_comparison(
    results: dict[str, AblationRunResult],
    gold: dict[str, list[int]] | None = None,
) -> str:
    """Format ablation results as a comparison table (Tables 1–4 from spec)."""
    gold = gold or TRIAL_GOLD
    gold_va_mean = sum(gold["CompACT-10"][i] for i in [1, 3, 6, 9]) / 4

    lines = [
        "=" * 75,
        "MentalRiskES Task 1 — Calibration Ablation Results",
        "Spec: mentalriskes2026_constraints_ablation_spec.md",
        "=" * 75,
    ]

    # Table 1: Final-round metrics per config
    lines += [
        "",
        "Table 1: Final-Round Metrics per Configuration",
        f"{'Config':<8} {'Anchors':>8} {'LvlB':>6} {'LvlC':>6} "
        f"{'PHQ9-RMSE':>10} {'GAD7-RMSE':>10} {'CACT-RMSE':>10} "
        f"{'MeanRMSE':>10} {'MeanPear':>10}",
        "-" * 75,
    ]

    for config_name in ["A0", "A1", "A2", "A3", "A4", "A5"]:
        if config_name not in results:
            continue
        run_cfg = ABLATION_CONFIGS[config_name]
        m = results[config_name].final_metrics(gold)
        phq9_r = m.get("PHQ-9", {}).get("rmse", float("nan"))
        gad7_r = m.get("GAD-7", {}).get("rmse", float("nan"))
        cact_r = m.get("CompACT-10", {}).get("rmse", float("nan"))
        mean_r = m.get("_overall", {}).get("mean_rmse", float("nan"))
        mean_p = m.get("_overall", {}).get("mean_pearson", float("nan"))

        anc = "Y" if run_cfg.prompt_anchors else "N"
        lvlb = "Y" if run_cfg.level_b else "N"
        lvlc = "Y" if run_cfg.level_c else "N"

        lines.append(
            f"{config_name:<8} {anc:>8} {lvlb:>6} {lvlc:>6} "
            f"{phq9_r:>10.3f} {gad7_r:>10.3f} {cact_r:>10.3f} "
            f"{mean_r:>10.3f} {mean_p:>10.3f}"
        )

    # Delta rows (official run comparisons)
    lines += ["", "Deltas (positive = Run N better than Run M):"]
    for label, c1, c2 in [
        ("Run0-Run1 (agent effect)", "A5", "A3"),
        ("Run1-Run2 (rules effect)", "A3", "A1"),
        ("Run0-Run2 (full stack)  ", "A5", "A1"),
    ]:
        if c1 in results and c2 in results:
            m1 = results[c1].final_metrics(gold)
            m2 = results[c2].final_metrics(gold)
            delta_mean = (m2.get("_overall", {}).get("mean_rmse", 0)
                         - m1.get("_overall", {}).get("mean_rmse", 0))
            lines.append(f"  {label}: mean RMSE delta={delta_mean:+.3f} (lower=better)")

    # Table 2: CompACT-10 subscale analysis
    lines += [
        "",
        "Table 2: CompACT-10 Subscale Analysis (VA bias is the primary calibration target)",
        f"{'Config':<8} {'OtE-RMSE':>10} {'BA-RMSE':>10} {'VA-RMSE':>10} "
        f"{'VA-bias':>10} {'B-viols':>8} {'C-corrs':>8}",
        "-" * 75,
    ]

    for config_name in ["A0", "A1", "A2", "A3", "A4", "A5"]:
        if config_name not in results:
            continue
        run_result = results[config_name]
        m = run_result.final_metrics(gold)
        cact = m.get("CompACT-10", {})
        ote_r = cact.get("subscale_openness_rmse", float("nan"))
        ba_r = cact.get("subscale_awareness_rmse", float("nan"))
        va_r = cact.get("subscale_valued_action_rmse", float("nan"))

        # VA bias: mean predicted VA - gold VA
        if run_result.final_result:
            pred_va = sum(run_result.final_result.compact10[i] for i in [1, 3, 6, 9]) / 4
            va_bias = pred_va - gold_va_mean
        else:
            va_bias = float("nan")

        total_b = sum(len(rr.level_b_violations) for rr in run_result.round_results)
        total_c = sum(len(rr.level_c_corrections) for rr in run_result.round_results)

        lines.append(
            f"{config_name:<8} {ote_r:>10.3f} {ba_r:>10.3f} {va_r:>10.3f} "
            f"{va_bias:>+10.2f} {total_b:>8} {total_c:>8}"
        )

    # Table 3: Per-round convergence summary (first round within ±3 of gold CompACT total)
    gold_compact_total = sum(gold["CompACT-10"])
    lines += [
        "",
        "Table 3: Convergence — first round where CompACT-10 total within ±3 of gold "
        f"({gold_compact_total})",
        f"{'Config':<8} {'Converge-Round':>15}",
        "-" * 25,
    ]

    for config_name in ["A0", "A1", "A2", "A3", "A4", "A5"]:
        if config_name not in results:
            continue
        converge_round = None
        for rr in results[config_name].round_results:
            if abs(sum(rr.compact10) - gold_compact_total) <= 3:
                converge_round = rr.round_number
                break
        lines.append(f"{config_name:<8} {str(converge_round) if converge_round else 'never':>15}")

    return "\n".join(lines)


