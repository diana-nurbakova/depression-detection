# ---------------------------------------------------------------------------

# PHQ-9: 9 items (0-3), approximate clinical item weights
# Order: anhedonia, depressed_mood, sleep, fatigue, appetite,
#        worthlessness, concentration, psychomotor, SI
_PHQ9_WEIGHTS = [1.1, 1.1, 1.0, 1.0, 0.9, 1.0, 0.9, 0.8, 0.5]

# GAD-7: 7 items (0-3), approximate clinical item weights
# Order: anxious, uncontrollable_worry, excessive_worry, relaxing,
#        restless, irritable, afraid
_GAD7_WEIGHTS = [1.1, 1.0, 1.0, 0.9, 1.0, 0.9, 0.8]

# CompACT-10 profile → (OtE_mean, BA_mean, VA_mean) per item (0-4 scale)
# Subscale indices: OtE=[2,4,7], BA=[0,5,8], VA=[1,3,6,9]
# Higher = more psychological flexibility
_COMPACT10_PROFILE_SUBSCALES: dict[str, tuple[float, float, float]] = {
    "low_flexibility":      (1.0, 1.5, 2.0),  # avoidant, disconnected from values
    "moderate_flexibility": (2.3, 2.5, 2.5),  # moderate across board
    "mixed_flexibility":    (1.5, 2.0, 2.8),  # values-aware but experientially avoidant
    "high_flexibility":     (3.2, 3.0, 3.2),  # fully flexible
}
_OTE_IDX = [2, 4, 7]
_BA_IDX = [0, 5, 8]
_VA_IDX = [1, 3, 6, 9]


def _distribute_total(
    total: int,
    weights: list[float],
    max_val: int,
) -> list[int]:
    """Distribute `total` across len(weights) items proportionally, capped at max_val.

    Deterministic: uses fraction-based rounding adjustment (no RNG).
    """
    n = len(weights)
    w_sum = sum(weights)
    raw = [total * w / w_sum for w in weights]
    scores = [min(max_val, max(0, round(r))) for r in raw]

    diff = total - sum(scores)
    if diff != 0:
        # Sort by fractional part to decide where to add/subtract
        fracs = sorted(range(n), key=lambda i: raw[i] - int(raw[i]), reverse=(diff > 0))
        for i in fracs:
            if diff == 0:
                break
            if diff > 0 and scores[i] < max_val:
                scores[i] += 1
                diff -= 1
            elif diff < 0 and scores[i] > 0:
                scores[i] -= 1
                diff += 1

    return scores


def derive_gold_from_metadata(metadata: dict) -> dict[str, list[int]]:
    """Derive item-level gold scores from simulation metadata.

    Args:
        metadata: contents of metadata.json, containing 'target_scores' with
            phq9_total (int), gad7_total (int), compact10_profile (str).

    Returns:
        dict with "PHQ-9" (9 ints, 0-3), "GAD-7" (7 ints, 0-3),
        "CompACT-10" (10 ints, 0-4).

    Note:
        The item distribution is clinically motivated but approximate —
        only the totals (PHQ-9 and GAD-7) and subscale profile (CompACT-10)
        are reliable ground truth from the simulator. RMSE numbers should
        be interpreted relative across configs, not as absolute accuracy.
    """
    target = metadata["target_scores"]
    phq9_total: int = target["phq9_total"]
    gad7_total: int = target["gad7_total"]
    compact10_profile: str = target.get("compact10_profile", "moderate_flexibility")

    # PHQ-9
    phq9 = _distribute_total(phq9_total, _PHQ9_WEIGHTS, max_val=3)

    # GAD-7
    gad7 = _distribute_total(gad7_total, _GAD7_WEIGHTS, max_val=3)

    # CompACT-10 — derive subscale totals from profile, then distribute across items
    ote_mean, ba_mean, va_mean = _COMPACT10_PROFILE_SUBSCALES.get(
        compact10_profile, (2.0, 2.0, 2.0),
    )
    ote_total = max(0, min(len(_OTE_IDX) * 4, round(ote_mean * len(_OTE_IDX))))
    ba_total = max(0, min(len(_BA_IDX) * 4, round(ba_mean * len(_BA_IDX))))
    va_total = max(0, min(len(_VA_IDX) * 4, round(va_mean * len(_VA_IDX))))

    ote_scores = _distribute_total(ote_total, [1.0] * len(_OTE_IDX), max_val=4)
    ba_scores = _distribute_total(ba_total, [1.0] * len(_BA_IDX), max_val=4)
    va_scores = _distribute_total(va_total, [1.0] * len(_VA_IDX), max_val=4)

    compact10 = [0] * 10
    for k, idx in enumerate(_OTE_IDX):
        compact10[idx] = ote_scores[k]
    for k, idx in enumerate(_BA_IDX):
        compact10[idx] = ba_scores[k]
    for k, idx in enumerate(_VA_IDX):
        compact10[idx] = va_scores[k]

    return {"PHQ-9": phq9, "GAD-7": gad7, "CompACT-10": compact10}


