def format_simulated_ablation_report(
    all_results: dict[str, dict[str, AblationRunResult]],
    all_gold: dict[str, dict[str, list[int]]],
    configs: list[str] | None = None,
) -> str:
    """Format multi-session simulated ablation report with mean ± std.

    Args:
        all_results: session_id -> {config_name -> AblationRunResult}.
        all_gold: session_id -> derived gold dict (for final_metrics calls).
        configs: ordered list of config names to include.
    """
    configs = configs or ["A0", "A1", "A3", "A5"]
    n_sessions = len(all_results)

    def _mean(vals: list[float]) -> float:
        valid = [v for v in vals if not math.isnan(v)]
        return sum(valid) / len(valid) if valid else float("nan")

    def _std(vals: list[float]) -> float:
        valid = [v for v in vals if not math.isnan(v)]
        if len(valid) < 2:
            return 0.0
        m = sum(valid) / len(valid)
        return math.sqrt(sum((v - m) ** 2 for v in valid) / (len(valid) - 1))

    # Collect metrics per config across sessions
    config_metrics: dict[str, dict[str, list[float]]] = {
        c: {"mean_rmse": [], "mean_pearson": [], "phq9_rmse": [], "gad7_rmse": [], "compact10_rmse": []}
        for c in configs
    }

    for session_id, session_results in all_results.items():
        gold = all_gold.get(session_id, {})
        for config_name in configs:
            if config_name not in session_results:
                continue
            m = session_results[config_name].final_metrics(gold) if gold else {}
            cm = config_metrics[config_name]
            cm["mean_rmse"].append(m.get("_overall", {}).get("mean_rmse", float("nan")))
            cm["mean_pearson"].append(m.get("_overall", {}).get("mean_pearson", float("nan")))
            cm["phq9_rmse"].append(m.get("PHQ-9", {}).get("rmse", float("nan")))
            cm["gad7_rmse"].append(m.get("GAD-7", {}).get("rmse", float("nan")))
            cm["compact10_rmse"].append(m.get("CompACT-10", {}).get("rmse", float("nan")))

    lines = [
        "=" * 85,
        "MentalRiskES Task 1 — Simulated Persona Ablation",
        f"Sessions: {n_sessions} | Configs: {', '.join(configs)}",
        "Note: item-level gold derived from phq9_total/gad7_total/compact10_profile",
        "      RMSE values are relative across configs, not absolute accuracy.",
        "=" * 85,
        "",
        "Aggregated Metrics across sessions (mean ± std):",
        f"{'Config':<8} {'Anchors':>8} {'LvlB':>6} {'LvlC':>6} "
        f"{'PHQ9-RMSE':>15} {'GAD7-RMSE':>15} {'CACT-RMSE':>15} {'MeanRMSE':>15}",
        "-" * 85,
    ]

    for config_name in configs:
        if config_name not in ABLATION_CONFIGS:
            continue
        run_cfg = ABLATION_CONFIGS[config_name]
        cm = config_metrics[config_name]

        anc = "Y" if run_cfg.prompt_anchors else "N"
        lvlb = "Y" if run_cfg.level_b else "N"
        lvlc = "Y" if run_cfg.level_c else "N"

        p9m, p9s = _mean(cm["phq9_rmse"]), _std(cm["phq9_rmse"])
        g7m, g7s = _mean(cm["gad7_rmse"]), _std(cm["gad7_rmse"])
        cam, cas = _mean(cm["compact10_rmse"]), _std(cm["compact10_rmse"])
        mrm, mrs = _mean(cm["mean_rmse"]), _std(cm["mean_rmse"])

        lines.append(
            f"{config_name:<8} {anc:>8} {lvlb:>6} {lvlc:>6} "
            f"{p9m:>7.3f}±{p9s:<6.3f} "
            f"{g7m:>7.3f}±{g7s:<6.3f} "
            f"{cam:>7.3f}±{cas:<6.3f} "
            f"{mrm:>7.3f}±{mrs:<6.3f}"
        )

    # Delta rows
    lines += ["", "Deltas (positive = improvement, lower RMSE):"]
    for label, c1, c2 in [
        ("A5 vs A3 (agent effect) ", "A5", "A3"),
        ("A3 vs A1 (rules effect) ", "A3", "A1"),
        ("A5 vs A0 (full stack)   ", "A5", "A0"),
    ]:
        if c1 in config_metrics and c2 in config_metrics:
            deltas = []
            for sid in all_results:
                gold = all_gold.get(sid, {})
                sr = all_results[sid]
                if c1 in sr and c2 in sr and gold:
                    m1 = sr[c1].final_metrics(gold).get("_overall", {}).get("mean_rmse", float("nan"))
                    m2 = sr[c2].final_metrics(gold).get("_overall", {}).get("mean_rmse", float("nan"))
                    if not (math.isnan(m1) or math.isnan(m2)):
                        deltas.append(m2 - m1)  # positive = c1 better
            if deltas:
                n_better = sum(1 for d in deltas if d > 0)
                lines.append(
                    f"  {label}: mean RMSE delta={_mean(deltas):+.3f}±{_std(deltas):.3f} "
                    f"({c1} better in {n_better}/{len(deltas)} sessions)"
                )

    # Per-session breakdown
    lines += [
        "",
        "Per-Session Breakdown (final-round mean RMSE):",
        f"{'Session':<32}" + "".join(f" {c:>10}" for c in configs),
        "-" * (32 + 11 * len(configs)),
    ]

    for session_id in sorted(all_results):
        gold = all_gold.get(session_id, {})
        row = f"{session_id:<32}"
        for config_name in configs:
            sr = all_results[session_id]
            if config_name in sr and gold:
                val = sr[config_name].final_metrics(gold).get("_overall", {}).get("mean_rmse", float("nan"))
                row += f" {val:>10.3f}"
            else:
                row += f" {'N/A':>10}"
        lines.append(row)

    return "\n".join(lines)


