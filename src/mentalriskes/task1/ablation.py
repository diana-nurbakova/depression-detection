def _uses_temporal(run_config: RunConfig) -> bool:
    """Check if any instrument uses temporal aggregation (not T0)."""
    return any(m != "T0" for m in [
        run_config.temporal_phq9,
        run_config.temporal_gad7,
        run_config.temporal_compact10,
    ])


def run_config_on_trial(
    run_config: RunConfig,
    trial_dir: str | Path,
    client: LLMClient,
    gold: dict[str, list[int]] | None = None,
) -> AblationRunResult:
    """Run a single ablation configuration on all trial rounds.

    Accumulates conversation history across rounds and produces one
    assessment per round. Returns AblationRunResult with per-round data.
    """
    from .assessors import assess_all_instruments
    from .calibration import calibrate_scores
    from .temporal import SessionPredictionHistory, apply_temporal_aggregation

    gold = gold or TRIAL_GOLD
    trial_data = load_trial_data(trial_dir)
    store = ConversationStore()

    # Prediction histories for temporal aggregation (keyed by session_id)
    histories: dict[str, SessionPredictionHistory] = {}
    use_temporal = _uses_temporal(run_config)

    result = AblationRunResult(
        config_name=run_config.name,
        description=run_config.description,
    )

    for round_n in sorted(trial_data.keys()):
        messages = trial_data[round_n]
        store.update_from_server_response(messages)

        for session_id, _ in messages.items():
            t0 = time.monotonic()
            context = store.get_context(session_id, max_turns=20)

            # Assessment (Level A injected here if prompt_anchors=True)
            assessments = assess_all_instruments(
                client,
                context,
                use_few_shot=run_config.few_shot,
                use_prompt_anchors=run_config.prompt_anchors,
            )

            phq9 = calibrate_scores(assessments["PHQ-9"].scores, "PHQ-9",
                                    run_config.calibration, run_config.calibration_params)
            gad7 = calibrate_scores(assessments["GAD-7"].scores, "GAD-7",
                                    run_config.calibration, run_config.calibration_params)
            compact10 = calibrate_scores(assessments["CompACT-10"].scores, "CompACT-10",
                                         run_config.calibration, run_config.calibration_params)

            # Level B
            violations: list[ConstraintViolation] = []
            if run_config.level_b:
                phq9, gad7, compact10, violations = apply_level_b_constraints(
                    phq9, gad7, compact10,
                )

            # Level C
            level_c_corrections: list[dict] = []
            if run_config.level_c and _should_invoke_level_c(
                violations, compact10, sum(phq9), sum(gad7),
            ):
                phq9, gad7, compact10, level_c_corrections = run_level_c_agent(
                    client, phq9, gad7, compact10, violations, assessments,
                )

            # Temporal aggregation
            if use_temporal:
                if session_id not in histories:
                    histories[session_id] = SessionPredictionHistory(session_id=session_id)
                history = histories[session_id]
                history.add_round(round_n, phq9, gad7, compact10)

                if history.n_rounds() >= 2:
                    agg = apply_temporal_aggregation(
                        history,
                        phq9_method=run_config.temporal_phq9,
                        gad7_method=run_config.temporal_gad7,
                        compact10_method=run_config.temporal_compact10,
                        decay=run_config.temporal_decay,
                        stability_threshold=run_config.temporal_stability_threshold,
                        w1_threshold_factor=run_config.temporal_w1_threshold,
                        discard_anomalous=run_config.temporal_discard_anomalous,
                    )
                    phq9 = agg["phq9"]
                    gad7 = agg["gad7"]
                    compact10 = agg["compact10"]

            elapsed = time.monotonic() - t0
            logger.info(
                "[%s] R%d %s: PHQ-9=%d GAD-7=%d CompACT-10=%d (%.1fs, %d B-violations)",
                run_config.name, round_n, session_id,
                sum(phq9), sum(gad7), sum(compact10), elapsed, len(violations),
            )

            result.round_results.append(RoundResult(
                session_id=session_id,
                round_number=round_n,
                phq9=phq9,
                gad7=gad7,
                compact10=compact10,
                level_b_violations=violations,
                level_c_corrections=level_c_corrections,
                elapsed_s=elapsed,
            ))

    return result


def run_ablation_study(
    configs: list[str] | None,
    trial_dir: str | Path,
    pipeline_cfg: MentalRiskESConfig,
    output_dir: str | Path = "runs/mentalriskes_ablation",
    gold: dict[str, list[int]] | None = None,
) -> dict[str, AblationRunResult]:
    """Run the full ablation study on trial data.

    Args:
        configs: list of config names to run (default: A0, A1, A3, A5).
          Full 6-config ablation: ["A0", "A1", "A2", "A3", "A4", "A5"].
          Quick 3-config (official runs only): ["A1", "A3", "A5"].
        trial_dir: path to trial data directory.
        pipeline_cfg: MentalRiskES pipeline config (for LLM settings).
        output_dir: directory to save per-config results.
        gold: gold scores (defaults to TRIAL_GOLD from evaluation.py).

    Returns:
        dict mapping config_name -> AblationRunResult.
    """
    gold = gold or TRIAL_GOLD
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)

    if configs is None:
        configs = ["A0", "A1", "A3", "A5"]  # default: baseline + official runs

    # Merge both config registries for lookup
    all_configs = {**ABLATION_CONFIGS, **TEMPORAL_ABLATION_CONFIGS}

    results: dict[str, AblationRunResult] = {}
    client = create_llm_client(pipeline_cfg.llm)

    for config_name in configs:
        if config_name not in all_configs:
            logger.warning("Unknown ablation config: %s (choices: %s)", config_name,
                           list(all_configs.keys()))
            continue

        run_config = all_configs[config_name]
        logger.info("=" * 60)
        logger.info("Running ablation %s: %s", config_name, run_config.description)
        logger.info("=" * 60)

        try:
            result = run_config_on_trial(run_config, trial_dir, client, gold)
            results[config_name] = result

            # Save per-config JSONL
            config_path = output_path / f"{config_name}_rounds.jsonl"
            with open(config_path, "w", encoding="utf-8") as f:
                for rr in result.round_results:
                    f.write(json.dumps({
                        "config": config_name,
                        "session_id": rr.session_id,
                        "round": rr.round_number,
                        "phq9": rr.phq9,
                        "gad7": rr.gad7,
                        "compact10": rr.compact10,
                        "level_b_violations": len(rr.level_b_violations),
                        "level_c_corrections": len(rr.level_c_corrections),
                        "elapsed_s": rr.elapsed_s,
                    }, ensure_ascii=False) + "\n")

            # Final metrics
            final_m = result.final_metrics(gold)
            logger.info(
                "%s final: PHQ-9 RMSE=%.3f, GAD-7 RMSE=%.3f, CompACT RMSE=%.3f, mean=%.3f",
                config_name,
                final_m.get("PHQ-9", {}).get("rmse", 0),
                final_m.get("GAD-7", {}).get("rmse", 0),
                final_m.get("CompACT-10", {}).get("rmse", 0),
                final_m.get("_overall", {}).get("mean_rmse", 0),
            )
        except Exception as e:
            logger.error("Ablation %s failed: %s", config_name, e, exc_info=True)

    # Save summary
    _save_ablation_summary(results, gold, output_path)

    return results


