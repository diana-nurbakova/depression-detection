def run_ablation_on_simulated(
    simulated_dir: str | Path,
    configs: list[str] | None,
    pipeline_cfg: MentalRiskESConfig,
    output_dir: str | Path = "runs/mentalriskes_simulated_ablation",
) -> dict[str, dict[str, AblationRunResult]]:
    """Run ablation on all simulated persona sessions.

    Args:
        simulated_dir: root directory containing one sub-dir per simulated
            session (each has round_N.json files + metadata.json).
        configs: ablation config names to run (default: A0, A1, A3, A5).
        pipeline_cfg: MentalRiskES pipeline config (for LLM settings).
        output_dir: directory to save per-session and aggregated results.

    Returns:
        dict mapping session_id -> {config_name -> AblationRunResult}.
        The derived gold for each session is embedded in AblationRunResult
        via per_round_metrics(gold) / final_metrics(gold).
    """
    simulated_dir = Path(simulated_dir)
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)

    if configs is None:
        configs = ["A0", "A1", "A3", "A5"]

    # Discover session directories
    session_dirs = sorted(
        d for d in simulated_dir.iterdir()
        if d.is_dir() and (d / "metadata.json").exists()
    )
    if not session_dirs:
        logger.warning("No simulated sessions found in %s", simulated_dir)
        return {}

    logger.info("Found %d simulated sessions in %s", len(session_dirs), simulated_dir)
    client = create_llm_client(pipeline_cfg.llm)

    # session_id -> config_name -> AblationRunResult
    all_results: dict[str, dict[str, AblationRunResult]] = {}
    # session_id -> derived gold (stored for reporting)
    all_gold: dict[str, dict[str, list[int]]] = {}

    for session_dir in session_dirs:
        meta_path = session_dir / "metadata.json"
        with open(meta_path, "r", encoding="utf-8") as f:
            metadata = json.load(f)

        session_id = metadata["session_id"]
        gold = derive_gold_from_metadata(metadata)
        all_gold[session_id] = gold

        profile_id = metadata.get("profile", {}).get("id", session_id)
        t = metadata["target_scores"]
        logger.info("=" * 60)
        logger.info("Session: %s (profile: %s)", session_id, profile_id)
        logger.info(
            "  Target PHQ-9=%d GAD-7=%d CompACT=%s | Derived gold: PHQ-9=%d GAD-7=%d CompACT=%d",
            t["phq9_total"], t["gad7_total"], t["compact10_profile"],
            sum(gold["PHQ-9"]), sum(gold["GAD-7"]), sum(gold["CompACT-10"]),
        )

        session_results: dict[str, AblationRunResult] = {}

        for config_name in configs:
            if config_name not in ABLATION_CONFIGS:
                logger.warning("Unknown ablation config: %s", config_name)
                continue

            run_config = ABLATION_CONFIGS[config_name]
            logger.info("  Config %s: %s", config_name, run_config.description)

            try:
                result = run_config_on_trial(run_config, session_dir, client, gold)
                session_results[config_name] = result

                m = result.final_metrics(gold)
                logger.info(
                    "  %s done: PHQ-9 RMSE=%.3f GAD-7 RMSE=%.3f CompACT RMSE=%.3f mean=%.3f",
                    config_name,
                    m.get("PHQ-9", {}).get("rmse", 0),
                    m.get("GAD-7", {}).get("rmse", 0),
                    m.get("CompACT-10", {}).get("rmse", 0),
                    m.get("_overall", {}).get("mean_rmse", 0),
                )
            except Exception as e:
                logger.error(
                    "Session %s config %s failed: %s", session_id, config_name, e,
                    exc_info=True,
                )

        all_results[session_id] = session_results

        # Save per-session summary
        sess_out = output_path / session_id
        sess_out.mkdir(exist_ok=True)
        _save_simulated_session_summary(session_id, session_results, gold, metadata, sess_out)

    # Save aggregated report
    report = format_simulated_ablation_report(all_results, all_gold, configs)
    report_path = output_path / "simulated_ablation_report.txt"
    report_path.write_text(report, encoding="utf-8")
    logger.info("Simulated ablation report saved to %s", report_path)
    print(report)

    return all_results


def _save_simulated_session_summary(
    session_id: str,
    session_results: dict[str, AblationRunResult],
    gold: dict[str, list[int]],
    metadata: dict,
    output_dir: Path,
) -> None:
    """Save per-session ablation summary JSON."""
    summary: dict[str, Any] = {
        "session_id": session_id,
        "profile": metadata.get("profile", {}),
        "target_scores": metadata.get("target_scores", {}),
        "derived_gold": {inst: scores for inst, scores in gold.items()},
        "configs": {},
    }

    for config_name, result in session_results.items():
        m = result.final_metrics(gold)
        summary["configs"][config_name] = {
            "description": result.description,
            "final_metrics": {
                inst: {k: round(v, 4) if isinstance(v, float) else v
                       for k, v in inst_m.items()
                       if isinstance(v, (int, float, bool, str))}
                for inst, inst_m in m.items()
                if isinstance(inst_m, dict)
            },
            "trajectory": result.per_round_metrics(gold),
        }

    path = output_dir / "ablation_summary.json"
    path.write_text(json.dumps(summary, indent=2, ensure_ascii=False))


