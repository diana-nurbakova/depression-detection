"""Ablation study framework for MentalRiskES Task 1 calibration tiers.

Simulated-persona ablation:
  Validates calibration tiers on synthetic patient sessions generated by
  data_prep/simulator.py. Each session directory contains round_N.json
  files (same format as trial data) plus metadata.json with target_scores:
    {phq9_total: int, gad7_total: int, compact10_profile: str}

  derive_gold_from_metadata() converts these totals + profile into
  item-level gold vectors for RMSE/Pearson computation. The distribution
  is deterministic (seeded on session_id) and clinically motivated
  (see _PHQ9_WEIGHTS, _GAD7_WEIGHTS, _COMPACT10_PROFILE_SUBSCALES).

  run_ablation_on_simulated() iterates over all session dirs in a given
  root, runs each ablation config, aggregates metrics across sessions.


Implements the 6-configuration ablation (A0–A5) from:
  specs/MentalRiskES/mentalriskes2026_constraints_ablation_spec.md

Two modes:

1. Full LLM ablation — runs each config end-to-end on trial data (LLM required).
   A0: baseline (no anchors, no B, no C)
   A1: prompt anchors only                      [= official Run 2]
   A2: Level B rules only (no anchors)
   A3: prompt anchors + Level B                 [= official Run 1]
   A4: Level C agent only (no anchors, no B)
   A5: full stack (anchors + B + C)             [= official Run 0]

2. Post-hoc calibration ablation — takes an existing prediction log
   (JSONL output of a prior trial run) and applies Level B and/or C
   to the stored raw scores. No new LLM calls for assessment.
   Useful for quickly testing B/C contribution without re-running the LLM.
   Note: cannot retroactively apply Level A (prompt anchors change the input).
"""

